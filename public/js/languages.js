/* =================================================
   LANGUAGE ENGINE (НЕ МЕНЯЛСЯ)
================================================= */

function resolveInitialLanguage() {
  const saved = localStorage.getItem("lang");
  if (saved && window.translations[saved]) return saved;
  const browserLang = navigator.language?.slice(0, 2);
  if (browserLang && window.translations[browserLang]) return browserLang;
  return window.I18N.defaultLang;
}

function translate(key) {
  const lang = window.I18N.currentLang || window.I18N.defaultLang;
  return window.translations[lang]?.[key] ?? key;
}

window.t = translate;

function applyTranslations(lang) {

  if (!window.translations[lang]) {
    lang = window.I18N.defaultLang;
  }

  window.I18N.currentLang = lang;
  localStorage.setItem("lang", lang);

  document.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = translate(el.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    el.placeholder = translate(el.dataset.i18nPlaceholder);
  });
}

window.applyTranslations = applyTranslations;

document.addEventListener("DOMContentLoaded", () => {
  applyTranslations(resolveInitialLanguage());
});